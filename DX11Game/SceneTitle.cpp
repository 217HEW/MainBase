//**************************************************************
// 初期化処理
//**************************************************************
HRESULT InitTitle()
{
	HRESULT hr = S_OK;

	// ポリゴン表示初期化
	hr = InitPolygon(GetDevice());
	if (FAILED(hr))
		return hr;

	// 中身はまだない

	return hr;
}

//**************************************************************
// 終了処理
//**************************************************************
void UninitTitle()
{
	// 中身無し

		// ポリゴン表示終了処理
	UninitPolygon();
}

//**************************************************************
// 更新処理
//**************************************************************
void UpdateTitle()
{
	if (GetKeyRelease(VK_1))
	{
		StartFadeOut(SCENE_GAME);
	}
	// ポリゴン表示更新
	UpdatePolygon();

}

